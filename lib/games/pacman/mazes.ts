const MAZES: Record<DifficultyLevel, MazeDef> = { 1: MAZE_1, 2: MAZE_2, 3: MAZE_3, 4: MAZE_4, 5: MAZE_5 };

export function getMazeDef(level: DifficultyLevel): MazeDef {
  return MAZES[level];
}

export function buildMaze(def: MazeDef): TileType[][] {
  return def.data.map(row => row.map(cell => {
    switch (cell) {
      case 0: return 'wall';
      case 1: return 'dot';
      case 2: return 'power';
      case 3: return 'empty';
      case 4: return 'ghostHouse';
      case 5: return 'tunnel';
      default: return 'wall';
    }
  }));
}

export function countDots(def: MazeDef): number {
  let count = 0;
  for (const row of def.data) for (const cell of row) if (cell === 1 || cell === 2) count++;
  return count;
}
